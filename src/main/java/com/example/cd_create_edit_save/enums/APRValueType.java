package com.example.cd_create_edit_save.enums;

/**
 * Enumeration representing the types of APR (Annual Percentage Rate)
 * configurations.
 * 
 * @author Krishna
 * @since 1.0
 */
public enum APRValueType {

	/**
	 * Represents a configuration where a single, fixed APR value is set.
	 */
	SPECIFIC,

	/**
	 * Represents a configuration where an APR range is defined.
	 */
	RANGE
}
